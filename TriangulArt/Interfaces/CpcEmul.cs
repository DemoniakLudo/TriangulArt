		public void POKE8(int adr, byte value) {
			VGA.POKE8(adr, value);
		}

		public void Run(int tc, List<Datas> l) {
			tailleColonnes = tc;
			lstData = l;
			bpStart.Enabled = bpReadData.Enabled = bpFillMemory.Enabled = txtDataAdr.Enabled = txbFillValue.Enabled = true;
			bpStop.Enabled = false;
			pictureBox1.Refresh();
			Application.DoEvents();
			ShowDialog();
		}

		private void InitCpc() {
			desasm.Init();
			Z80.Init();
			PPI.Init();
			CRTC.Init();
			VGA.CopyMemory(Code3D, 0x200);

			// Init table des adresses écran
			int width = tailleColonnes == 0 ? 0x40 : tailleColonnes == 1 ? 0x50 : 0x60;
			int nbLignes = 16384 / width;
			int adrEcr = 0x8000;
			int adr = 0x600;
			for (int l = 0; l < nbLignes; l++) {
				VGA.POKE16((ushort)adr, (ushort)adrEcr);
				adr += 2;
				adrEcr += 0x800;
				if ((adrEcr & 0x4000) != 0)
					adrEcr = adrEcr - 0x4000 + width;   // 'BC26'
			}

			// Init couleurs
			for (int i = 0; i < 17; i++)
				VGA.SetColor(i, (PaletteCpc.GetColorPal(i < 16 ? i : 0).GetColorArgb));

			// Init registres CRTC
			CRTC.RegsCRTC[1] = width >> 1;
			CRTC.RegsCRTC[2] = 0x1A + (width >> 2);
			CRTC.RegsCRTC[6] = width == 0x40 ? 0x20 : width == 0x50 ? 0x19 : 0x15;
			CRTC.RegsCRTC[7] = 0x12 + (CRTC.RegsCRTC[6] >> 1);

			// Copie des données de l'animation
			adr = 0x800;
			foreach (Datas data in lstData) {
				for (int i = 0; i < data.lstTriangle.Count; i++) {
					Triangle t = data.lstTriangle[i];
					if (t.enabled) {
						POKE8(adr++, (byte)t.x1);
						POKE8(adr++, (byte)t.y1);
						POKE8(adr++, (byte)t.x2);
						POKE8(adr++, (byte)t.y2);
						POKE8(adr++, (byte)t.x3);
						POKE8(adr++, (byte)t.y3);
						POKE8(adr++, (byte)(i < data.lstTriangle.Count - 1 ? t.color : t.color + 0x80));
					}
				}
			}
			POKE8(adr++, 0xFF);	// Fin d'animation (obligatoire)

			Z80.PC.Word = 0x200;
		}

		private void BpStart_Click(object sender, EventArgs e) {
			bpStart.Enabled = bpReadData.Enabled = bpFillMemory.Enabled = txtDataAdr.Enabled = txbFillValue.Enabled = false;
			bpStop.Enabled = true;
			finMain = false;
			int nbCycles = 0;
			long curTime = 0, oldTime = 0;
			InitCpc();
			while (!finMain) {
				int cycle = Z80.ExecInstr();
				nbCycles += cycle;
				bool DoResync = CRTC.CycleCRTC(cycle);
				if ((DoResync && nbCycles > 1000) || nbCycles > 100000) {
					do {
						curTime = Stopwatch.GetTimestamp() / 10;
						pictureBox1.Refresh();
						Application.DoEvents();
					}
					while (curTime - oldTime < nbCycles);
					oldTime = curTime;
					nbCycles = 0;
					string str = "";
					desasm.SetLigne(Z80.PC.Word, ref str);
					Instr.Text = str;
					AF.Text = Z80.AF.Word.ToString("X4");
					BC.Text = Z80.BC.Word.ToString("X4");
					DE.Text = Z80.DE.Word.ToString("X4");
					HL.Text = Z80.HL.Word.ToString("X4");
					AF_.Text = Z80._AF.Word.ToString("X4");
					BC_.Text = Z80._BC.Word.ToString("X4");
					DE_.Text = Z80._DE.Word.ToString("X4");
					HL_.Text = Z80._HL.Word.ToString("X4");
					PC.Text = Z80.PC.Word.ToString("X4");
					SP.Text = Z80.SP.Word.ToString("X4");
					IX.Text = Z80.IX.Word.ToString("X4");
					IY.Text = Z80.IY.Word.ToString("X4");
					I.Text = Z80.IR.High.ToString("X2");
					R.Text = Z80.IR.Low.ToString("X2");
					IM.Text = Z80.InterruptMode.ToString();
				}
			}
		}

		private void BpStop_Click(object sender, EventArgs e) {
			bpStart.Enabled = bpReadData.Enabled = bpFillMemory.Enabled = txtDataAdr.Enabled = txbFillValue.Enabled = true;
			bpStop.Enabled = false;
			finMain = true;
		}

		private void BpExit_Click(object sender, EventArgs e) {
			finMain = true;
			Hide();
		}

		private void BpReadData_Click(object sender, EventArgs e) {
			int adr = int.Parse(txtDataAdr.Text, System.Globalization.NumberStyles.HexNumber);
			if (adr >= 0x4000 && adr < 0x8000) {
				OpenFileDialog dlg = new OpenFileDialog { Filter = "Fichiers binaire (*.bin)|*.bin|Fichiers écrans CPC (*.scr)|*.scr" };
				if (dlg.ShowDialog() == DialogResult.OK) {
					FileStream fileScr = new FileStream(dlg.FileName, FileMode.Open, FileAccess.Read);
					byte[] tabBytes = new byte[fileScr.Length];
					fileScr.Read(tabBytes, 0, tabBytes.Length);
					fileScr.Close();
					if (Cpc.CheckAmsdos(tabBytes))
						Array.Copy(tabBytes, 0x80, tabBytes, 0, tabBytes.Length - 0x80);

					VGA.CopyMemory(tabBytes, adr);
				}
			}
			else
				MessageBox.Show("L'adresse de chargement doit être comprise entre #4000 et #7FFF", "Erreur");
		}

		private void BpFillMemory_Click(object sender, EventArgs e) {
			int value = int.Parse(txbFillValue.Text, System.Globalization.NumberStyles.HexNumber);
			if (value >= 0 && value <= 255)
				VGA.FillMemory((byte)value);
		}
